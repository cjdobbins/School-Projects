public class quiz {
  
  public void floatLoop() {
    float n = 3.0f;
    int i = 0;
    do {
    n *= 10.0;
    System.out.println(n);
    i += 1;
    } while(i < 20);
  }
  
  public void doubleLoop() {
    double n = 3;
    int i = 0;
    do {
      n *= 10.0;
      System.out.println(n);
      i += 1;
    } while(i < 50);
    
  }
  
}